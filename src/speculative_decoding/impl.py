if __name__ == "__main__":
    parser = argparse.ArgumentParser()
    parser.add_argument("--prompt", type=str, default="The meaning of life is")
    parser.add_argument("--K", type=int, default=4,
                        help="Number of draft tokens per round")
    parser.add_argument("--max-new-tokens", type=int, default=100)
    parser.add_argument("--temperature", type=float, default=1.0)
    parser.add_argument("--draft-model", type=str, default="gpt2")
    parser.add_argument("--target-model", type=str, default="gpt2-xl")
    parser.add_argument("--device", type=str, default="mps")
    args = parser.parse_args()

    tokenizer, draft, target = load_models(
        args.draft_model, args.target_model, args.device
    )

    run_comparison(args, tokenizer, draft, target, args.device)